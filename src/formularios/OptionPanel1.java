    private void botonCalcularNotaActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_botonCalcularNotaActionPerformed
    {//GEN-HEADEREND:event_botonCalcularNotaActionPerformed
        double[] notas = new double[9];
        double[] resultados = new double[2];

        try
        {

            notas[0] = Double.parseDouble(epe1Field.getText());
            notas[1] = Double.parseDouble(epe2Field.getText());
            notas[2] = Double.parseDouble(epe3Field.getText());
            notas[3] = Double.parseDouble(epe4Field.getText());
            notas[4] = Double.parseDouble(eva1Field.getText());
            notas[5] = Double.parseDouble(eva2Field.getText());
            notas[6] = Double.parseDouble(eva3Field.getText());
            notas[7] = Double.parseDouble(eva4Field.getText());
            notas[8] = Double.parseDouble(examenField.getText());

        }
        catch (Exception Ex)
        {

            JOptionPane.showMessageDialog(null, "¡Error! \n\n"
                    + Ex.getMessage(), "Advertencia",
                    JOptionPane.WARNING_MESSAGE);

        }

        if (examenField.isEnabled())
        {
            calculo = new Ponderaciones(notas, true);
            resultados = calculo.Ponderar32();
            notaPresLabel.setText(Double.toString(resultados[0]));
            notaFinalLabel.setText(Double.toString(resultados[1]));
            if (resultados[1] >= 4.0)
            {
                estadoLabel.setText("APROBADO");
            }
            else
            {
                estadoLabel.setText("REPROBADO");
            }
        }
        else if (!examenField.isEnabled())
        {
            calculo = new Ponderaciones(notas, false);
            resultados = calculo.Ponderar32();
            notaPresLabel.setText(Double.toString(resultados[0]));
            notaFinalLabel.setText(Double.toString(resultados[1]));
            if (resultados[1] >= 4.0)
            {
                estadoLabel.setText("APROBADO");
            }
            else
            {
                estadoLabel.setText("REPROBADO");
            }
        }
    }//GEN-LAST:event_botonCalcularNotaActionPerformed

