    /**
     * Esta funcion sirve para escuchar los eventos de cada text field donde van
     * las notas, se inicializa y se guardan en un array
     */
    public void focusListener()
    {

        epe1Field.addFocusListener(this);
        epe2Field.addFocusListener(this);
        epe3Field.addFocusListener(this);
        epe4Field.addFocusListener(this);
        eva1Field.addFocusListener(this);
        eva2Field.addFocusListener(this);
        eva3Field.addFocusListener(this);
        eva4Field.addFocusListener(this);
        epesField[0] = epe1Field;
        epesField[1] = epe2Field;
        epesField[2] = epe3Field;
        epesField[3] = epe4Field;
        evasField[0] = eva1Field;
        evasField[1] = eva2Field;
        evasField[2] = eva3Field;
        evasField[3] = eva4Field;

    }

    public void ExamenChecker()
    {

        try
        {
            /**
             * Almacenar todas las notas de las epes de los text fields
             * correspondientes
             */
            int count = 0;
            for (JTextField i : epesField)
            {

                if (Double.parseDouble(i.getText()) != 0)
                {
                    epeNotas[count] = Double.parseDouble(i.getText());
                    count++;
                }
                if (i.getText().equals(""))
                {
                    i.setText(Double.toString(0));
                }

            }

            /**
             * Almacenar todas las notas de las evas de los text fields
             * correspondientes
             */
            count = 0;
            for (JTextField i : evasField)
            {

                if (Double.parseDouble(i.getText()) != 0)
                {
                    evaNotas[count] = Double.parseDouble(i.getText());
                    count++;
                }
                if (i.getText().equals(""))
                {
                    i.setText(Double.toString(0));
                }

            }

            /**
             * Calcular todas las evas y ver si da rojo para almacenar su
             * resultado como una nota epe en epeNotas[4]
             */
            double sumador = 0;
            for (int i = 0; i < evaNotas.length; i++)
            {

                sumador += evaNotas[i];

            }
            epeNotas[4] = sumador / 2;

            /**
             * Revisar si todas las notas son mayores a 4.0 para poder
             * interactuar con la interfaz y informar al usuario de su situacion
             * en tiempo real
             */
            count = 0;
            for (int i = 0; i < 5; i++)
            {
                if (epeNotas[i] < 4.0 && epeNotas[i] != 0)
                {
                    examenField.setEnabled(true);
                    derechoLabel.setText("SI");
                    break;
                }
                else
                {
                    count++;
                }
            }

            if (count == 5)
            {
                examenField.setEnabled(false);
                examenField.setText("0");
                derechoLabel.setText("NO");
            }

        }
        catch (Exception ex)
        {
            for (JTextField i : epesField)
            {
                    i.setText(Double.toString(0));
            }
            
            for (JTextField i : evasField)
            {
                i.setText(Double.toString(0));
            }
                       
            JOptionPane.showMessageDialog(null, "No puedes colocar caracteres "
                    + "incorrectos ni ocupar \"comas\" en los campos para "
                    + "ingresar las notas ni dejarlos vacios \n\nError: "
                    + ex.getMessage(),
                    "Advertencia", JOptionPane.WARNING_MESSAGE);
            
            
        }

    }

    @Override
    public void focusGained(FocusEvent e)
    {
        ExamenChecker();
    }

    @Override
    public void focusLost(FocusEvent e)
    {
        ExamenChecker();
    }

